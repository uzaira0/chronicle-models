public enum class  ChronicleUsageEventType ( public val  value: Int) {
    NONE(0x00000000),
    MOVE_TO_FOREGROUND(0x00000001),
    MOVE_TO_BACKGROUND(0x00000002),
    ACTIVITY_PAUSED(MOVE_TO_BACKGROUND.value),
    ACTIVITY_RESUMED(MOVE_TO_FOREGROUND.value),
    CONFIGURATION_CHANGE(0x00000005),
    DEVICE_SHUTDOWN(0x0000001a),
    DEVICE_STARTUP(0x0000001b),
    USER_INTERACTION(0x00000007),
    SHORTCUT_INVOCATION(0x00000008),
    SCREEN_INTERACTIVE(0x0000000F),
    SCREEN_NON_INTERACTIVE(0x00000010),
    KEYGUARD_SHOWN(0x00000011),
    FOREGROUND_SERVICE_START(0x00000013),
    FOREGROUND_SERVICE_STOP(0x00000014),
    KEYGUARD_HIDDEN(0x00000012),
    ACTIVITY_STOPPED(0x00000017)

}